MAX_EP = {"m²":300,"m³":500,"m":600,"t":500,"st":30000,"kg":100,"ps":50000}

def _norm(u):
    u = u.lower().strip().replace(".", "")
    return {"m2":"m²","m3":"m³","qm":"m²","cbm":"m³","stk":"st","stck":"st",
            "stück":"st","lfm":"m","lfdm":"m","lm":"m","tonne":"t","to":"t",
            "pauschal":"ps","psch":"ps","pau":"ps","einh":"st"}.get(u, u)

def validate_price(ep, bezeichnung, einheit, source=""):
    if ep <= 0: return False, "Preis ist 0 oder negativ"
    bez = bezeichnung.lower()
    eu = _norm(einheit)
    mx = MAX_EP.get(eu, 50000)
    if ep > mx: return False, f"EP {ep:.2f} > Max {mx}"
    for (mat, unit), (lo, hi) in PRICE_RANGES.items():
        if mat in bez and unit == eu:
            if ep < lo * 0.25: return False, f"EP {ep:.2f} zu niedrig fuer {mat} ({lo}-{hi})"
            if ep > hi * 3.0: return False, f"EP {ep:.2f} zu hoch fuer {mat} ({lo}-{hi})"
            break
    schuett = ("schotter","kies","splitt","sand","frostschutz","tragschicht","brechsand","mineralbeton")
    if eu == "m²" and any(k in bez for k in schuett) and ep > 50:
        return False, f"Schuettgut {ep:.2f}/m2 unrealistisch"
    if "anpassen" in bez and eu == "m" and ep > 60:
        return False, f"Anpassen {ep:.2f}/m unrealistisch"
    return True, "OK"

def validate_match(m):
    return validate_price(m.get("ep",0), m.get("bezeichnung",""), m.get("lv_einheit",""))

def validate_component_addition(haupt, neben, lv_pos=None):
    ht = (haupt.get("offer_text","") or "").lower()
    nt = (neben.get("offer_text","") or "").lower()
    valid = ("beton","fundament","unterbeton","bettung","fugenmaterial",
            "muffe","schweissmuffe","rost","abdeckung","zargen","moertel",
            "dichtung","kleber","anker","verbindung","dichtring")
    if any(k in nt for k in valid): return True
    same = ("pflaster","bordstein","bord","platte","rinne","rohr",
           "schotter","kies","splitt","sand","stein","belag",
           "palisade","l-stein","zaun","gelaender",
           "folie","noppenbahn","vlies","geotextil","drainage","drainmatte",
           "mulch","hackschnitzel","substrat","erde","boden",
           "baum","strauch","hecke","staude","gras")
    for pt in same:
        if pt in ht and pt in nt: return False
    return True
